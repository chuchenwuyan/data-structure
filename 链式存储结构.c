// 线性表的链式表示：链表

// 链表使用物理位置任意的存储单元存放元素
// 每个结点通常由数据域 data 和指针域 next 构成
// data 存放数据元素
// next 存放下一个结点整体的起始地址

// 单链表：每个结点只有一个指针域 next
// 双链表：每个结点有两个指针域 prior 和 next
// 循环链表：尾结点的指针不再指向 NULL，而是指回链表前面某个结点

// 头指针：指向链表起始结点的指针
// 有头结点时：头指针指向头结点
// 无头结点时：头指针指向首元结点

// 头结点：位于首元结点之前的辅助结点
// 头结点通常不存有效数据，也可以存放链表长度等附加信息
// 有头结点时，头结点的 next 指向首元结点
// 有头结点的空表：head->next == NULL

// 首元结点：第一个真正存放数据元素的结点

// 尾结点：最后一个数据结点
// 普通单链表中，尾结点的 next == NULL

// 头结点的好处：
// 让首元结点也有前驱结点，便于统一处理插入和删除操作
// 尤其是插入、删除第一个数据元素时，不需要单独特殊处理 head

// 双链表结点结构：
// [prior | data | next]
// prior：指向前一个结点
// next ：指向后一个结点

