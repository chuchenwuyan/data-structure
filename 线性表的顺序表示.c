// 查找算法：顺序查找
// 思路：从表的一端开始，逐个元素与key比较
// 找到：返回该元素的位置；没找到：返回0或-1，按题目要求
//
// 比较次数：
// 最好情况：第1个元素就是key，比较1次
// 最坏情况：最后一个才找到，或查找失败，比较n次
//
// 若查找成功，且每个元素被查找的概率相等：
// 平均查找长度 ASL = (1 + 2 + ... + n) / n = (n + 1) / 2
//
// 时间复杂度：
// 最好 O(1)，最坏 O(n)，平均 O(n)