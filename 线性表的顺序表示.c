// 查找算法：顺序查找
// 思路：从表的一端开始，逐个元素与key比较
// 找到：返回该元素的位置；没找到：返回0或-1，按题目要求
//
// 比较次数：
// 最好情况：第1个元素就是key，比较1次
// 最坏情况：最后一个才找到，或查找失败，比较n次
//
// 若查找成功，且每个元素被查找的概率相等：
// 平均查找长度 ASL = (1 + 2 + ... + n) / n = (n + 1) / 2
//
// 时间复杂度：
// 最好 O(1)，最坏 O(n)，平均 O(n)

// 插入算法
// 顺序表插入元素时，需要先判断插入位置是否合法，再判断表是否已满
//
// 若当前表长为n：
// 数组下标视角：合法插入位置是 0 ~ n
// 逻辑位序视角：合法插入位置是 1 ~ n+1
//
// 在中间或前面插入元素时，要从最后一个元素开始，从后往前依次后移一位
// 直到空出目标位置，再把新元素放进去
//
// 移动次数：
// 尾插：移动0次，最好情况 O(1)
// 头插：移动n次，最坏情况 O(n)
// 平均移动次数：n / 2
// 平均时间复杂度仍然是 O(n)

